// dynamic typing

// variable a is assigned to a string
let a = "hello";
console.log(typeof a);

// variable a is assigned to an integer
a = 5
console.log(typeof a)


// weak typing
// tries to convert types, this is common in interpreted languages, typing is less strict
console.log(1 + '7'); // => '17'

const output = 1 + '7'
console.log(typeof output)